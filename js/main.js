/* Main Function to replace DOM texts based on selected language */
function changeLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    /* Update document language for accessibility and SEO */
    document.documentElement.lang = lang;
}

/* Initialization, Translation Toggle, and Form Submission Event Handling */
document.addEventListener('DOMContentLoaded', () => {
    
    /* 1. Set default language to English */
    let currentLang = 'en';
    changeLanguage(currentLang);

    /* 2. Language Toggle Button Logic */
    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        
        /* Initial button text indicates the alternative language option */
        langToggleBtn.textContent = 'ES';

        langToggleBtn.addEventListener('click', () => {
            currentLang = currentLang === 'en' ? 'es' : 'en';
            langToggleBtn.textContent = currentLang === 'en' ? 'ES' : 'EN';
            changeLanguage(currentLang);
        });
    }

    /* 3. General Form Handling (Contact / Login) */
    const contactForm = document.getElementById('contactForm');
    if (contactForm) {
        contactForm.addEventListener('submit', (event) => {
            event.preventDefault();
            alert('Your message has been sent successfully to the SpotTrack team.');
            event.target.reset();
        });
    }
    
    const loginForm = document.getElementById('loginForm');
    if (loginForm) {
        loginForm.addEventListener('submit', (event) => {
            event.preventDefault();
            alert('Login successful. Redirecting to dashboard...');
        });
    }
});